    private void btnSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSessionActionPerformed
        if (StringUtils.isEmpty(UserSession.getUsername())) {
            
            Login dp = new Login();
            dp.setVisible(true);
            dispose();
            
        }else{
            //Se cerrara la sesión
            UserSession.clearSession();

            Dashboard dp = new Dashboard();
            dp.setVisible(true);
            dispose();
            
        }        

    }//GEN-LAST:event_btnSessionActionPerformed

