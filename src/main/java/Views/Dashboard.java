    /* Load information */
    private void loadInformation(){
        if (UserSession.getRole() == 2) isAdmin = true;
        else{   
            isAdmin = false;   
            btnMantenimientos.setEnabled(false);
            btnUsuarios.setEnabled(false);
            btnOrdersPan.setEnabled(false);
            btnReports.setEnabled(false);
            lblWelcome.setVisible(false);
        }
        
        if(StringUtils.isEmpty(UserSession.getUsername()))  btnSession.setText("Iniciar sesión");
        else btnSession.setText("Cerrar sesión");
        
    }
    
    private void initContet() {
        switchPanel("Inicio");
    }

